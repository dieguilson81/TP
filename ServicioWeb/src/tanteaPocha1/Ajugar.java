package tanteaPocha;

public class Ajugar {
	private int idpartida;
	private boolean ascendente;
	private boolean descendente;
	private boolean conpinta;
	private boolean sinpinta;
	private boolean subasta;
	private boolean manopinta;
	private boolean oros;
    public int getIdpartida() {
		return idpartida;
	}
	public void setIdpartida(int idpartida) {
		this.idpartida = idpartida;
	}
	public boolean isAscendente() {
		return ascendente;
	}
	public void setAscendente(boolean ascendente) {
		this.ascendente = ascendente;
	}
	public boolean isDescendente() {
		return descendente;
	}
	public void setDescendente(boolean descendente) {
		this.descendente = descendente;
	}
	public boolean isConpinta() {
		return conpinta;
	}
	public void setConpinta(boolean conpinta) {
		this.conpinta = conpinta;
	}
	public boolean isSinpinta() {
		return sinpinta;
	}
	public void setSinpinta(boolean sinpinta) {
		this.sinpinta = sinpinta;
	}
	public boolean isSubasta() {
		return subasta;
	}
	public void setSubasta(boolean subasta) {
		this.subasta = subasta;
	}
	public boolean isManopinta() {
		return manopinta;
	}
	public void setManopinta(boolean manopinta) {
		this.manopinta = manopinta;
	}
	public boolean isOros() {
		return oros;
	}
	public void setOros(boolean oros) {
		this.oros = oros;
	}
}
